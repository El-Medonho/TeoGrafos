#include "bits/stdc++.h"

using namespace std;

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n,m; cin >> n >> m;

    vector<vector<pair<int,int>>> graph(n);

    for(int i = 0; i < m; i++){
        int a,b,c; cin >> a >> b >> c; a--; b--;
        graph[a].push_back({b,c});
        graph[b].push_back({a,c});
    }

    priority_queue<pair<int,int>, vector<pair<int,int>>, greater<pair<int,int>>> pq;

    vector<int> dist(n, 1e9+50), par(n,0);
    vector<bool> vis(n,0);

    pq.push({0,0});     //vou usar o vértice 0 como inicial

    while(!pq.empty()){
        int cc = pq.top().second; pq.pop();
        
        if(vis[cc]) continue;
        vis[cc] = 1;

        for(auto [nxt, w]: graph[cc]){
            if(vis[nxt] || dist[cc] + w >= dist[nxt]) continue;

            dist[nxt] = dist[cc] + w;
            par[nxt] = cc;
            pq.push({dist[nxt], nxt});
        }
    }

    int querry; cin >> querry;

    while(querry--){
        int req; cin >> req; req--;     //aqui o usuario insere um vertice para obter o caminho minimo entre 0 e req

        vector<int> ans(0);

        while(req != par[req]){         
            ans.push_back(req);
            req = par[req];
        }
        ans.push_back(req);

        reverse(ans.begin(), ans.end());

        for(int i: ans) cout << i+1 << ' ';
        cout << '\n';
    }

    return 0;
}