#include "bits/stdc++.h"

using namespace std;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n,m; cin >> n >> m;     
    vector<vector<int>> graph(n);

    while(m--){
        int a,b; cin >> a >> b;
        a--; b--; //0 indexed
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    stack<int> stk;
    vector<bool> vis(n,0), in(n,0);

    function<bool(int, int)> dfs;
    int cycle = -1; // guardar o início do ciclo

    dfs = [&](int cc, int last){
        in[cc] = 1;
        stk.push(cc);

        for(int j: graph[cc]){
            if(vis[j] || j == last) continue;

            if(in[j]){
                cycle = j;
                return 1;
            }

            if(dfs(j, cc)) return 1;
        }

        in[cc] = 0;
        vis[cc] = 1;

        return 0;
    } ;

    if(!dfs(0, -1)){            //Considerando que o grafo e conexo
        cout << "E uma arvore!\n";
        return 0;
    }

    vector<int> ans(0);
    int u = -1;
    do{
        u = stk.top(); stk.pop();

        ans.push_back(u+1);     //voltando a ser 1 indexed
    }

    while(u != cycle);

    int h = ans.size();

    cout << "Tamanho do ciclo: " << h << '\n';

    for(int i = 0; i < h; i++) cout << ans[i] << " \n"[i == h-1];

    return 0;
}